package logical;

public class MathClassExample {

	public static void main(String[] args) {
		int x=8;
		int y=9;
//		To find largest number from using math method
		System.out.println("largest Number is:"  + Math.max(x, y));
		
		
//		To find square root of number using math method
		System.out.println("square root of y :" + Math.sqrt(y));
		
		
//		To find smallest number using math method
		System.out.println("smallest Number is:" + Math.min(x,y));

	}

}
